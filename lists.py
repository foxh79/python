names = ['Foxh', 'Mwau', 'Akym']
print(names)

#accessinng first index
print(names[0])

#accessing last index
print([-1])

#inserting value at a specific index
names.insert(3, 'Ali')
print(names)

#changing an element in a [articular index
names[1] = 'Natalie'
print(names)

#pop removes last element
names.pop()
print(names)

#deleting an element
names.remove('Foxh')
print(names)